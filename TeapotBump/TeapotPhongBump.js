function drawTeapot() {
    
    var canvas=document.getElementById("gl-canvas");
    gl=WebGLUtils.setupWebGL(canvas);
    
    if (!gl) { alert( "WebGL is not available" ); }
    
    gl.viewport( 0, 0, canvas.width, canvas.height );
    
    gl.clearColor( 1.0, 1.0, 1.0, 1.0 );
    
    gl.enable( gl.DEPTH_TEST );
    
    myShaderProgram =
    initShaders( gl,"vertex-shader", "fragment-shader" );
    gl.useProgram( myShaderProgram );

    
    var vertices = getVertices();
    var indexList = getFaces();
    
    numVertices=531;
    numTriangles=1062;
    //numVertices=1738;
    //numTriangles=3170;
    
    var faceNormals=getFaceNormals( vertices, indexList, numTriangles );
    var vertexNormals=getVertexNormals( vertices, indexList, faceNormals,
                                       numVertices, numTriangles );
    
    var p0 = gl.getUniformLocation( myShaderProgram, "p0" );
    gl.uniform3f( p0, .0, .0, 0.6 ); // point light source location at 0.0, 0.0, 0.5
    
    var ka = gl.getUniformLocation( myShaderProgram, "ka" ); // coefficients for object
    var kd = gl.getUniformLocation( myShaderProgram, "kd" );
    var ks = gl.getUniformLocation( myShaderProgram, "ks" );
    gl.uniform3f( ka, 0.5, 0.5, 0.5 ); // ambient coefficients
    gl.uniform3f( kd, 0.5, 0.5, 0.5 ); // diffuse coefficients
    gl.uniform3f( ks, 1.0, 1.0, 1.0 ); // specular coefficients
    
    var Ia = gl.getUniformLocation( myShaderProgram, "Ia" ); // values for light source
    var Id = gl.getUniformLocation( myShaderProgram, "Id" );
    var Is = gl.getUniformLocation( myShaderProgram, "Is" );
    gl.uniform3f( Ia, .1, .1, .1 ); // ambient part of incident light
    gl.uniform3f( Id, .8, .8, .8 ); // diffuse part of incident light
    gl.uniform3f( Is, .8, .8, .8 ); // specular part of incident light
    
    var alpha = gl.getUniformLocation( myShaderProgram, "alpha" );
    gl.uniform1f( alpha, 10.0 ); // shininess coefficient
    
    // The following snippet of code programmatically generates
    // a random texture to use as the normal map.
    // However, you can load in a pre-created texture as well.
    // Random texture
    var b = new Array();
    var sz=2048;
    for (var i=0; i<sz; i++) {
        b[i]=new Array();
        for (var j=0; j< sz; j++ ) {
            b[i][j] = Math.random();
        }
    }
    
    /// Store partial derivatives in normal map
    var normals = new Uint8Array( 3 * sz * sz );
    
    for (var i=0; i<sz; i++ ) {
        for (var j=0; j<sz; j++) {
            if (i<sz-1 && j<sz-1) {
                c1 = b[i+1][j]-b[i][j];
                c2 = b[i][j+1]-b[i][j];
                c3 = 1.0;
                normals[3*sz*i+3*j+0]=255*(0.5*(c1+1.0));
                normals[3*sz*i+3*j+1]=255*(0.5*(c2+1.0));
                normals[3*sz*i+3*j+2]=c3;
            } else {
                normals[3*sz*i+3*j+0]=0.0;
                normals[3*sz*i+3*j+1]=0.0;
                normals[3*sz*i+3*j+2]=1.0;
            }
        
        }
    }
    
    console.log(normals[3]);
    console.log(normals[4]);
    console.log(normals[5]);
    
    textureRandom = gl.createTexture();
    gl.bindTexture( gl.TEXTURE_2D, textureRandom );
    gl.texImage2D( gl.TEXTURE_2D, 0, gl.RGB, sz, sz, 0, gl.RGB, gl.UNSIGNED_BYTE, normals );
    gl.texParameteri(gl.TEXTURE_2D, gl.TEXTURE_MAG_FILTER, gl.LINEAR);
    gl.texParameteri(gl.TEXTURE_2D, gl.TEXTURE_MIN_FILTER, gl.LINEAR);
    gl.uniform1i( gl.getUniformLocation(myShaderProgram,"texMap"),0);

    var iBuffer = gl.createBuffer();
    gl.bindBuffer(gl.ELEMENT_ARRAY_BUFFER,iBuffer);
    gl.bufferData(gl.ELEMENT_ARRAY_BUFFER,new Uint16Array(indexList), gl.STATIC_DRAW);
    
    var vertexbuffer = gl.createBuffer();
    gl.bindBuffer(gl.ARRAY_BUFFER,vertexbuffer);
    gl.bufferData(gl.ARRAY_BUFFER, flatten(vertices), gl.STATIC_DRAW);
    
    var vertexPosition = gl.getAttribLocation(myShaderProgram,"vertexPosition");
    gl.vertexAttribPointer( vertexPosition, 4, gl.FLOAT, false, 0, 0 );
    gl.enableVertexAttribArray( vertexPosition );
    
    var normalsbuffer = gl.createBuffer();
    gl.bindBuffer(gl.ARRAY_BUFFER,normalsbuffer);
    gl.bufferData(gl.ARRAY_BUFFER, flatten(vertexNormals), gl.STATIC_DRAW);
    
    var vertexNormal = gl.getAttribLocation(myShaderProgram,"nv");
    gl.vertexAttribPointer( vertexNormal, 3, gl.FLOAT, false, 0, 0 );
    gl.enableVertexAttribArray( vertexNormal );
    
    
    
    render();
}

